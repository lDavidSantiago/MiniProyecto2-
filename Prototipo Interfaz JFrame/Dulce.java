
public class Dulce {
    String nombre;
    String tipoDulce;
    Dulce(String nombre,String tipoDulce){
        this.nombre = nombre;
        this.tipoDulce = tipoDulce;

    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getTipoDulce() {
        return tipoDulce;
    }

    public void setTipoDulce(String tipoDulce) {
        this.tipoDulce = tipoDulce;
    }
    
}
